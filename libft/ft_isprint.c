#include "libft.h"

int	ft_isprint(int c)
{
	return (31 < c && c < 127);
}

int	ft_isspace(char c)
{
	return (c == ' ' || c == '\f' || c == '\n' || c == '\r'
		|| c == '\t' || c == '\v');
}